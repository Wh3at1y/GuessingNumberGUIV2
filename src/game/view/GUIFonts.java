package game.view;

import java.awt.Font;

public class GUIFonts
	{
		public Font boldFont()
		{
			Font boldFont = new Font("Monaco", Font.BOLD, 16);
			return boldFont;
		}
		
		public Font slimFont()
		{
			Font slimFont = new Font("Century Gothic", Font.PLAIN, 14);
			return slimFont;
		}
		
		public Font impossibleFont()
		{
			Font impossibleFont = new Font("Monaco", Font.BOLD, 14);
			return impossibleFont;
		}
	}
